import Foundation


extension UIColor {
    class func alertViewBlue() -> UIColor {
        return UIColor(red:0.17, green:0.41, blue:0.74, alpha:1.0)
    }
    class func textGray() -> UIColor {
        return UIColor(red:0.51, green:0.57, blue:0.61, alpha:1.0)
    }
    
}
